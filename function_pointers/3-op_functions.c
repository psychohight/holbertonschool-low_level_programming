#include "3-calc.h"

/**
 * op_add - aditions.
 * @a: nbr.
 * @b: nbr.
 *
 * Return: resultat.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - soustraction.
 * @a: nbr.
 * @b: nbr.
 *
 * Return: resultat.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication.
 * @a: nbr.
 * @b: nbr.
 *
 * Return: resultat.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division.
 * @a: nbr.
 * @b: nbr.
 *
 * Return: resultat.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo.
 * @a: nbr.
 * @b: nbr.
 *
 * Return: resultat.
 */
int op_mod(int a, int b)
{
	return (a % b);
}